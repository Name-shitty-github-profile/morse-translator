def encode(content) -> str:
  co: str = ''
  for letter in content.upper(): co += f'{Morse[letter]} '
  return co

def decode(content) -> str:
  deco: str = ''
  for thing in content.split(' '): 
    if thing != '': deco += f'{Letter[thing]}'
  return deco.lower()
